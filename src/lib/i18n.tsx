type I18nContext = {
  language: Language;
  lang: Language;
  setLanguage: (language: Language) => void;
  setLang: (language: Language) => void;
  toggleLanguage: () => void;
  t: (key: string, params?: Record<string, string | number>) => string;
};

function isLanguage(value: string | null): value is Language {
  return value === "en" || value === "hi";
}

function readSavedLanguage(): Language {
  if (typeof window === "undefined") return "en";
  const saved = window.localStorage.getItem(STORAGE_KEY) ?? window.localStorage.getItem(LEGACY_STORAGE_KEY);
  return isLanguage(saved) ? saved : "en";
}

function resolveTranslation(tree: TranslationTree, key: string): string | undefined {
  const value = key.split(".").reduce<TranslationValue | undefined>((current, part) => {
    if (current && typeof current === "object" && part in current) return current[part];
    return undefined;
  }, tree);

  return typeof value === "string" ? value : undefined;
}

function interpolate(value: string, params?: Record<string, string | number>) {
  if (!params) return value;
  return Object.entries(params).reduce(
    (text, [key, replacement]) => text.replaceAll(`{{${key}}}`, String(replacement)),
    value,
  );
}

const I18nCtx = createContext<I18nContext>({
  language: "en",
  lang: "en",
  setLanguage: () => {},
  setLang: () => {},
  toggleLanguage: () => {},
  t: (key) => key,
});

export function LangProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => readSavedLanguage());

  const setLanguage = useCallback((nextLanguage: Language) => {
    setLanguageState(nextLanguage);
    if (typeof window !== "undefined") {
      window.localStorage.setItem(STORAGE_KEY, nextLanguage);
      window.localStorage.removeItem(LEGACY_STORAGE_KEY);
      document.documentElement.lang = nextLanguage;
    }
  }, []);

  useEffect(() => {
    document.documentElement.lang = language;
    window.localStorage.setItem(STORAGE_KEY, language);
  }, [language]);

  const t = useCallback(
    (key: string, params?: Record<string, string | number>) => {
      const translated =
        resolveTranslation(translations[language], key) ??
        resolveTranslation(translations.en, key) ??
        key;
      return interpolate(translated, params);
    },
    [language],
  );

  const value = useMemo<I18nContext>(
    () => ({
      language,
      lang: language,
      setLanguage,
      setLang: setLanguage,
      toggleLanguage: () => setLanguage(language === "en" ? "hi" : "en"),
      t,
    }),
    [language, setLanguage, t],
  );

  return <I18nCtx.Provider value={value}>{children}</I18nCtx.Provider>;
}

export const useLang = () => useContext(I18nCtx);
export const useLanguage = useLang;
